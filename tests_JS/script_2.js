let factorielle;
let ouloulou;
let result = 1;
let i = 1;

factorielle = prompt("De quel nombre veut tu calculer la factorielle ?");

ouloulou = factorielle

while (i <= ouloulou) {
factorielle = ouloulou - i + 1;
result = result * factorielle;
i = i + 1;
}

console.log(`Voilà le résultat de ta factorielle : ${result}`);